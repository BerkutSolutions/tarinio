def translate_value(value: str, lang: str) -> str:
    if not isinstance(value, str):
        return value
    exact = EXACT.get(lang, {})
    if value in exact:
        return exact[value]
    translated = value
    for source, target in sorted(PHRASES.get(lang, []), key=lambda item: len(item[0]), reverse=True):
        translated = translated.replace(source, target)
    return translated


def build_locale(lang: str) -> dict:
    base = json.loads(EN_PATH.read_text(encoding="utf-8"))
    key_overrides = KEY_EXACT.get(lang, {})
    out = {}
    for key, value in base.items():
        if key in key_overrides:
            out[key] = key_overrides[key]
            continue
        out[key] = translate_value(value, lang)
    return out


def main() -> None:
    for lang in ("de", "sr", "zh"):
        out = build_locale(lang)
        path = I18N_DIR / f"{lang}.json"
        path.write_text(json.dumps(out, ensure_ascii=False, indent=2, sort_keys=True) + "\n", encoding="utf-8")
        changed = sum(1 for key, value in out.items() if value != json.loads(EN_PATH.read_text(encoding="utf-8"))[key])
        print(f"{lang}: wrote {len(out)} keys, translated {changed}")


if __name__ == "__main__":
    main()
